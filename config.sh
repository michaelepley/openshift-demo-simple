OPENSHIFT_USERS_RHSADEMO=(OPENSHIFT_USER_RHSADEMO_MEPLEY OPENSHIFT_USER_RHSADEMO_MEPLEY2 OPENSHIFT_USER_RHSADEMO_MEPLEY_DEV OPENSHIFT_USER_RHSADEMO_MEPLEY_TEST OPENSHIFT_USER_RHSADEMO_MEPLEY_PROD)

